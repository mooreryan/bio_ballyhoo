use std::convert::TryFrom;
use std::error::Error;
use std::fmt;
use std::str::FromStr;

fn ascii_upcase(byte: u8) -> u8 {
    match (b'a'..=b'z').contains(&byte) {
        true => byte - 32,
        false => byte,
    }
}

#[derive(Debug)]
pub struct ParseBlosumMatrixError;

impl fmt::Display for ParseBlosumMatrixError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "BlosumMatrix parse error!")
    }
}

impl Error for ParseBlosumMatrixError {}

// Consider just making this copy.
#[derive(Clone, Copy, Debug)]
pub enum BlosumMatrix {
    Blosum45,
    Blosum50,
    Blosum62,
    Blosum80,
    Blosum90,
}

impl FromStr for BlosumMatrix {
    type Err = ParseBlosumMatrixError;

    fn from_str(string: &str) -> Result<Self, Self::Err> {
        match string {
            "blosum45" => Ok(Self::Blosum45),
            "blosum50" => Ok(Self::Blosum50),
            "blosum62" => Ok(Self::Blosum62),
            "blosum80" => Ok(Self::Blosum80),
            "blosum90" => Ok(Self::Blosum90),
            _ => Err(ParseBlosumMatrixError),
        }
    }
}

#[derive(Debug)]
pub struct ParseMinScoreError;

impl fmt::Display for ParseMinScoreError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "MinScore parse error!")
    }
}

impl Error for ParseMinScoreError {}

#[derive(Clone, Copy, Debug)]
pub enum MinScore {
    Zero,
    One,
}

impl MinScore {
    pub fn to_i8(&self) -> i8 {
        match &self {
            Self::Zero => 0,
            Self::One => 1,
        }
    }
}

impl TryFrom<i8> for MinScore {
    type Error = &'static str;

    fn try_from(value: i8) -> Result<Self, Self::Error> {
        match value {
            0 => Ok(MinScore::Zero),
            1 => Ok(MinScore::One),
            _ => Err("MinScore must be 0 or 1"),
        }
    }
}

impl FromStr for MinScore {
    // hi
    type Err = ParseMinScoreError;

    fn from_str(string: &str) -> Result<Self, Self::Err> {
        match string {
            "0" => Ok(Self::Zero),
            "1" => Ok(Self::One),
            _ => Err(ParseMinScoreError),
        }
    }
}

